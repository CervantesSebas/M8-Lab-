#Takes two inputs and determines whether the sum is greater, less than, or equal to 10
ans = int(input("First input: "))
ans2 = int(input("Second input: "))
num = (10)
if ans + ans2 == num: 
    print ("The sum of your inputs is equal to 10")
elif ans + ans2 > num:
    print ("The sum of your inputs is greater than 10")
elif ans + ans2 < num:
    print ("The sum of your inputs is less than 10")    